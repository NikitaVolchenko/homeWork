package com.company;

class movie {

    String name;
    int partsCount;
    String director;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    int getPartsCount() {
        return partsCount;
    }

    void setPartsCount(int partsCount) {
        this.partsCount = partsCount;
    }

    String getDirector() {
        return director;
    }

    void setDirector(String director) {
        this.director = director;
    }
}
